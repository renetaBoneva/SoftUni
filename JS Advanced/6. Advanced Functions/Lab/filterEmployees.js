function filter(data, criteria) {
    data = JSON.parse(data);
    let [index, value] = criteria.split('-')
    let filtered = data.filter(e => {
        if (criteria === 'all') {
            return e;
        } else {
            return e[index] === value;
        }
    })
    for (let i = 0; i < filtered.length; i++) {
        let current = filtered[i];
        console.log(`${i}. ${current["first_name"]} ${current["last_name"]} - ${current["email"]}`);
    }
}

